coisa = input('Digite qualquer palavra, letra ou número: ')
print('O tipo primitivo informado é: ', type(coisa))
print('Essa palavra, letra ou número informado, contém somente espaços?',coisa.isspace())
print('Essa palavra, letra ou número informado, é um número?', coisa.isnumeric())
print('Essa palavra, letra ou número informado, é alfabético?', coisa.isalpha())
print('Essa palavra, letra ou número informado, é alfanumérico?', coisa.isalnum())
print('Essa palavra, letra ou número informado, está em letras maiúsculas?', coisa.isupper())
print('Essa palavra, letra ou número informado, está em letras minusculas?', coisa.islower())
print('Essa palavra, letra ou número informado, está capitalizada?', coisa.istitle())







